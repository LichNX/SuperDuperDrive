package com.udacity.jwdnd.course1.cloudstorage.controller;

import com.udacity.jwdnd.course1.cloudstorage.model.User;
import com.udacity.jwdnd.course1.cloudstorage.services.UserService;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.ui.ModelMap;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.ModelAttribute;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.servlet.ModelAndView;
import org.springframework.web.servlet.mvc.support.RedirectAttributes;
import org.springframework.web.servlet.view.RedirectView;

import javax.servlet.http.HttpServletRequest;

@Controller
public class SignupController {

    private UserService userService;

    public SignupController(UserService userService) {
        this.userService = userService;
    }

    @GetMapping("/signup")
    public String getSignupPage( Model model ) {
        return "signup";
    }

    @PostMapping("/signup")
    public RedirectView signupUser(@ModelAttribute User user, RedirectAttributes attributes, Model model) {
        String signupError = null;

        if (!userService.isUsernameAvailable(user.getUserName())) {
            signupError = "The username already exists.";
        }

        if (signupError == null) {
            int rowsAdded = userService.createUser(user);
            if (rowsAdded < 0) {
                signupError = "There was an error signing you up. Please try again.";
            }
        }

        if (signupError == null) {
            RedirectView redirectView = new RedirectView("login", true);
            attributes.addFlashAttribute("signupSuccess", true);
            return redirectView;
        } else {
            attributes.addAttribute("signupSuccess", signupError);
        }
        RedirectView redirectView = new RedirectView("signup", true);
        return redirectView;
    }

}
